	/// fades out when hidden, using the specified duration.
	case fadeInOut(FadeInOutTransitionSettings)

	public init(from decoder: Decoder) throws {
		self = try dcuiDecodePoly(decoder, payloadKey: "settings") { t, c, k in
			switch t {
			case "FadeInOut": return .fadeInOut(try c.decode(FadeInOutTransitionSettings.self, forKey: k))
			default: return nil
			}
		}
	}
}

