/// Transition types for carousel/progression animations. MUST be an object
/// with a "type" field and a "settings" field. Use to control how carousel
/// items animate between slides.
public enum CarouselTransition: Codable, Hashable {
	/// A sliding transition where items slide in and out horizontally.
	/// Provides a classic carousel feel.
	case slideInOut(CarouselSlideInOutTransitionSettings)
	/// A fade transition where items cross-fade between each other.
	/// Provides a smoother, less directional transition.
	case fadeInOut(CarouselFadeInOutTransitionSettings)

	enum CodingKeys: String, CodingKey, Codable {
		case slideInOut = "SlideInOut",
			fadeInOut = "FadeInOut"
	}

	private enum ContainerCodingKeys: String, CodingKey {
		case type, settings
	}

	public init(from decoder: Decoder) throws {
		let container = try decoder.container(keyedBy: ContainerCodingKeys.self)
		if let type = try? container.decode(CodingKeys.self, forKey: .type) {
			switch type {
			case .slideInOut:
				if let content = try? container.decode(CarouselSlideInOutTransitionSettings.self, forKey: .settings) {
					self = .slideInOut(content)
					return
				}
			case .fadeInOut:
				if let content = try? container.decode(CarouselFadeInOutTransitionSettings.self, forKey: .settings) {
					self = .fadeInOut(content)
					return
				}
			}
		}
		throw DecodingError.typeMismatch(CarouselTransition.self, DecodingError.Context(codingPath: decoder.codingPath, debugDescription: "Wrong type for CarouselTransition"))
	}

	public func encode(to encoder: Encoder) throws {
		var container = encoder.container(keyedBy: ContainerCodingKeys.self)
		switch self {
		case .slideInOut(let content):
			try container.encode(CodingKeys.slideInOut, forKey: .type)
			try container.encode(content, forKey: .settings)
		case .fadeInOut(let content):
			try container.encode(CodingKeys.fadeInOut, forKey: .type)
			try container.encode(content, forKey: .settings)
		}
	}
}

