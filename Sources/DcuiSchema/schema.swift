/// A transition that applies only when an element enters (becomes visible).
/// MUST be an object with a "type" field and a "settings" field.
/// Currently only "FadeIn" is supported.
public enum InTransition: Decodable {
	/// A fade-in transition. The element gradually becomes visible over the
	/// specified duration.
	case fadeIn(FadeInTransitionSettings)

	public init(from decoder: Decoder) throws {
		self = try dcuiDecodePoly(decoder, payloadKey: "settings") { t, c, k in
			switch t {
			case "FadeIn": return .fadeIn(try c.decode(FadeInTransitionSettings.self, forKey: k))
			default: return nil
			}
		}
	}
}

