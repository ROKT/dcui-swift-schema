	case slideInOut(CarouselSlideInOutTransitionSettings)
	/// A fade transition where items cross-fade between each other.
	/// Provides a smoother, less directional transition.
	case fadeInOut(CarouselFadeInOutTransitionSettings)

	public init(from decoder: Decoder) throws {
		self = try dcuiDecodePoly(decoder, payloadKey: "settings") { t, c, k in
			switch t {
